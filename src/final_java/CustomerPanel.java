    private void refreshCustomerTable() {
        model.setRowCount(0); // Clear table
        for (Customer customer : hotelManager.getCustomers()) {
            model.addRow(new Object[]{
                customer.getId(),
                customer.getName(),
                customer.getGender(),
                customer.getPhoneNumber()
            });
        }
    }

