    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        try {
            String id = txt_id.getText().trim();
            String name = txt_name.getText().trim();
            String gender = cbxGender.getSelectedItem().toString();
            String phoneNumber = txt_phone_number.getText().trim();

            // Validate inputs
            if (id.isEmpty() || name.isEmpty() || phoneNumber.isEmpty()) {
                throw new IllegalArgumentException("All fields are required.");
            }
            if (!phoneNumber.matches("\\d+")) {
                throw new IllegalArgumentException("Phone number must contain only digits.");
            }

            // Create and add customer
            Customer customer = new Customer(id, name, gender, phoneNumber);
            hotelManager.addCustomer(customer);
            refreshCustomerTable();
            clearForm();
            JOptionPane.showMessageDialog(this, "Customer added successfully!");
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Add Customer Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnAddActionPerformed

