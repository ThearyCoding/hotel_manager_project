    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        try {
            int row = tblcustomer_information.getSelectedRow();
            if (row == -1) {
                throw new IllegalArgumentException("Please select a customer to update.");
            }

            String id = txt_id.getText().trim();
            String name = txt_name.getText().trim();
            String gender = cbxGender.getSelectedItem().toString();
            String phoneNumber = txt_phone_number.getText().trim();

            // Validate inputs
            if (name.isEmpty() || phoneNumber.isEmpty()) {
                throw new IllegalArgumentException("Name and phone number are required.");
            }
            if (!phoneNumber.matches("\\d+")) {
                throw new IllegalArgumentException("Phone number must contain only digits.");
            }

            // Update customer
            Customer updatedCustomer = new Customer(id, name, gender, phoneNumber);
            hotelManager.updateCustomer(updatedCustomer);
            refreshCustomerTable();
            clearForm();
            JOptionPane.showMessageDialog(this, "Customer updated successfully!");
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Update Customer Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnupdateActionPerformed

